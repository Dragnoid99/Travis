#The fuction that adds 2 numbers

def add ( x , y ) :
    return x + y;

#The fuction that subtracts 2 numbers

def subtract ( x , y ) :

    return x - y;

#The fuction that multiplies 2 numbers    

def multiply ( x , y ) :
    return x * y;

#The fuction that divides 2 numbers    

def divide ( x , y ) :
    return x / y;
